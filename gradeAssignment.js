//if else if
let grade = 80;

console.log("Enter Your grade:", grade);
//using if else if to determine the entered grade
if (grade>=90){
    console.log("Excellent.");
} else if(grade>=85){
    console.log("Good.");
} else if(grade>=75){
    console.log("Fair.");
} else {
    console.log("Needs Improvement.");
}